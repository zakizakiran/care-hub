    private void displayObat(List<ObatModel> medicines) {
        bodyPanel.removeAll();
        bodyPanel.repaint();
        bodyPanel.revalidate();

        if (medicines == null || medicines.isEmpty()) {
            emptyDataDisplay();
        } else {
            bodyPanel.setLayout(null);

            int x = 20, y = 20;
            int cardWidth = 285, cardHeight = 420;
            int padding = 20;
            int cardsPerRow = 3;

            for (int i = 0; i < medicines.size(); i++) {
                ObatModel obat = medicines.get(i);
                ShadowPanel cardObat = new ShadowPanel(8, Color.decode("#DFDFDF"));
                cardObat.setBackground(Color.WHITE);
                cardObat.setBounds(x, y, cardWidth, cardHeight);

                JLabel labelId = new JLabel("ID: " + obat.getId());
                JLabel titleNama = new JLabel("Nama Obat");
                JLabel labelNama = new JLabel(obat.getNama());
                JLabel titleKategori = new JLabel("Kategori");
                JLabel labelKategori = new JLabel(obat.getKategori());
                JLabel titleExp = new JLabel("Expired");
                JLabel labelExp = new JLabel(obat.getExp());
                JLabel titleHarga = new JLabel("Harga");
                JLabel labelHarga = new JLabel("Rp " + String.valueOf(obat.getHarga()));
                JLabel titleStok = new JLabel("Stok");
                JLabel labelStok = new JLabel(String.valueOf(obat.getStok()));

                RoundedPanel buttonEdit = new RoundedPanel(12, Color.white);
                JLabel editLabel = new JLabel("Edit Obat");
                editLabel.setForeground(Color.WHITE);
                buttonEdit.add(editLabel);
                buttonEdit.setCursor(new java.awt.Cursor(java.awt.Cursor.HAND_CURSOR));

                RoundedPanel buttonHapus = new RoundedPanel(12, Color.white);
                JLabel hapusLabel = new JLabel("Hapus Obat");
                hapusLabel.setForeground(Color.RED); // Set text color
                buttonHapus.add(hapusLabel);
                buttonHapus.setCursor(new java.awt.Cursor(java.awt.Cursor.HAND_CURSOR));

                setupHoverEffect(buttonEdit, Color.decode("#508D4E"), Color.BLACK);
                setupHoverEffect(buttonHapus, Color.WHITE, Color.RED);

                buttonEdit.addMouseListener(new java.awt.event.MouseAdapter() {
                    @Override
                    public void mouseClicked(java.awt.event.MouseEvent evt) {
                        editObatView = new EditObatView(obat);
                        editObatView.setVisible(true);
                    }
                });

                buttonHapus.addMouseListener(new java.awt.event.MouseAdapter() {
                    @Override
                    public void mouseEntered(java.awt.event.MouseEvent evt) {
                        hapusLabel.setForeground(Color.WHITE);
                    }

                    @Override
                    public void mouseExited(java.awt.event.MouseEvent evt) {
                        hapusLabel.setForeground(Color.RED);
                    }

                    @Override
                    public void mouseClicked(java.awt.event.MouseEvent evt) {
                        int response = JOptionPane.showConfirmDialog(null, "Anda yakin ingin menghapus obat " + obat.getNama(), "Konfirmasi", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
                        if (response == JOptionPane.YES_OPTION) {
                            obatController.hapusObat(obat.getId());

                            loadDataObat();
                        }
                    }
                });

                try {
                    File poppinsSemiBold = new File("src/resources/assets/fonts/Poppins-SemiBold.ttf");
                    File poppinsBold = new File("src/resources/assets/fonts/Poppins-Bold.ttf");

                    Font cardTxtStyle = Font.createFont(Font.TRUETYPE_FONT, poppinsSemiBold).deriveFont(14f);
                    Font titleTxtStyle = Font.createFont(Font.TRUETYPE_FONT, poppinsBold).deriveFont(12f);

                    titleNama.setFont(titleTxtStyle);
                    titleNama.setForeground(Color.decode("#508D4E"));
                    titleKategori.setFont(titleTxtStyle);
                    titleKategori.setForeground(Color.decode("#508D4E"));
                    titleExp.setFont(titleTxtStyle);
                    titleExp.setForeground(Color.decode("#508D4E"));
                    titleHarga.setFont(titleTxtStyle);
                    titleHarga.setForeground(Color.decode("#508D4E"));
                    titleStok.setFont(titleTxtStyle);
                    titleStok.setForeground(Color.decode("#508D4E"));

                    labelId.setForeground(Color.decode("#508D4E"));
                    labelId.setFont(titleTxtStyle);
                    labelNama.setFont(cardTxtStyle);
                    labelKategori.setFont(cardTxtStyle);
                    labelExp.setFont(cardTxtStyle);
                    labelHarga.setFont(cardTxtStyle);
                    labelStok.setFont(cardTxtStyle);
                    hapusLabel.setFont(cardTxtStyle);
                    editLabel.setFont(cardTxtStyle);

                } catch (Exception e) {
                    e.printStackTrace();
                }

                buttonHapus.setPreferredSize(new java.awt.Dimension(240, 36));
                buttonEdit.setPreferredSize(new java.awt.Dimension(240, 36));

                cardObat.setLayout(new org.netbeans.lib.awtextra.AbsoluteLayout());
                cardObat.add(labelId, new org.netbeans.lib.awtextra.AbsoluteConstraints(20, 20, -1, -1));
                cardObat.add(titleNama, new org.netbeans.lib.awtextra.AbsoluteConstraints(20, 50, -1, -1));
                cardObat.add(labelNama, new org.netbeans.lib.awtextra.AbsoluteConstraints(20, 70, -1, -1));
                cardObat.add(titleKategori, new org.netbeans.lib.awtextra.AbsoluteConstraints(20, 100, -1, -1));
                cardObat.add(labelKategori, new org.netbeans.lib.awtextra.AbsoluteConstraints(20, 120, -1, -1));
                cardObat.add(titleExp, new org.netbeans.lib.awtextra.AbsoluteConstraints(20, 150, -1, -1));
                cardObat.add(labelExp, new org.netbeans.lib.awtextra.AbsoluteConstraints(20, 170, -1, -1));
                cardObat.add(titleHarga, new org.netbeans.lib.awtextra.AbsoluteConstraints(20, 200, -1, -1));
                cardObat.add(labelHarga, new org.netbeans.lib.awtextra.AbsoluteConstraints(20, 220, -1, -1));
                cardObat.add(titleStok, new org.netbeans.lib.awtextra.AbsoluteConstraints(20, 250, -1, -1));
                cardObat.add(labelStok, new org.netbeans.lib.awtextra.AbsoluteConstraints(20, 270, -1, -1));
                cardObat.add(buttonEdit, new org.netbeans.lib.awtextra.AbsoluteConstraints(20, 320, -1, -1));
                cardObat.add(buttonHapus, new org.netbeans.lib.awtextra.AbsoluteConstraints(20, 360, -1, -1));

                bodyPanel.add(cardObat);

                if ((i + 1) % cardsPerRow == 0) {
                    x = 20;
                    y += cardHeight + padding;
                } else {
                    x += cardWidth + padding;
                }
            }

            bodyPanel.setPreferredSize(new Dimension(bodyPanel.getWidth(), y + cardHeight + padding));
            bodyPanel.revalidate();
            bodyPanel.repaint();
        }
    }

    private void setupHoverEffect(JPanel button, Color defaultColor, Color hoverColor) {
        button.setBackground(defaultColor);

        button.addMouseListener(new MouseAdapter() {
            @Override
            public void mouseEntered(MouseEvent e) {
                button.setBackground(hoverColor);
            }

            @Override
            public void mouseExited(MouseEvent e) {
                button.setBackground(defaultColor);
            }
        });
    }

