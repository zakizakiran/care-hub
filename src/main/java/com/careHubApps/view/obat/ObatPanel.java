    private void performSearch() {
        String searchText = searchTxtField.getText().trim().toLowerCase();

        if (searchText.isEmpty()) {
            loadDataObat();
            displayObat(obatList);
        } else {
            List<ObatModel> filteredPatients = obatList.stream()
                    .filter(o -> o.getNama().toLowerCase().contains(searchText)
                    || o.getId().toLowerCase().contains(searchText)
                    || o.getKategori().toLowerCase().contains(searchText))
                    .collect(Collectors.toList());

            if (filteredPatients.isEmpty()) {
                displayNoDataFound();
            } else {
                displayObat(filteredPatients);
            }
        }
    }

    public void loadDataObat() {
        this.obatList = obatController.getAllObat();
        if (obatList == null || obatList.isEmpty()) {
            emptyDataDisplay();
        } else {
            displayObat(obatList);
        }
    }

